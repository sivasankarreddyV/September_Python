# Program to check if a key exists in a dictionary

student = {
    "name": "David",
    "age": 24,
    "course": "Chemistry"
}

# Check if 'name' exists as a key
key_exists = "name" in student
print("Does 'name' key exist?", key_exists)

# Check if 'grade' exists as a key
key_exists = "grade" in student
print("Does 'grade' key exist?", key_exists)
