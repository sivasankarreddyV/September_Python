# Define two numbers
a = 10
b = 5

# Addition
addition = a + b
print(f"{a} + {b} = {addition}")

# Subtraction
subtraction = a - b
print(f"{a} - {b} = {subtraction}")

# Multiplication
multiplication = a * b
print(f"{a} * {b} = {multiplication}")

# Division
division = a / b
print(f"{a} / {b} = {division}")

# Modulus
modulus = a % b
print(f"{a} % {b} = {modulus}")

# Exponentiation
exponentiation = a ** b
print(f"{a} ** {b} = {exponentiation}")

# Floor Division
floor_division = a // b
print(f"{a} // {b} = {floor_division}")

