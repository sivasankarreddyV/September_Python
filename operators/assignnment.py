a = 10

# Assignment
a += 5  # Equivalent to a = a + 5
print(f"a += 5: {a}")

a -= 3  # Equivalent to a = a - 3
print(f"a -= 3: {a}")

a *= 2  # Equivalent to a = a * 2
print(f"a *= 2: {a}")

a /= 4  # Equivalent to a = a / 4
print(f"a /= 4: {a}")

a %= 3  # Equivalent to a = a % 3
print(f"a %= 3: {a}")

a **= 2  # Equivalent to a = a ** 2
print(f"a **= 2: {a}")

a //= 3  # Equivalent to a = a // 3
print(f"a //= 3: {a}")

